# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def generate_invoice_pdf(invoice_data: dict) -> str:
    """
    Render an invoice dict to a PDF file and return the absolute file path.

    The file is written to ``{settings.UPLOAD_DIR}/pdfs/``. The directory
    is created automatically when it does not yet exist.

    :param invoice_data: Dictionary containing all invoice fields (see
        ``invoice_service.send_invoice`` for the exact shape).
    :returns: Absolute path of the generated PDF, or an empty string when
        WeasyPrint is unavailable.
    :raises OSError: If the PDF directory cannot be created or the file
        cannot be written.
    """
    try:
        from weasyprint import HTML  # type: ignore[import-untyped]
    except ImportError:
        logger.warning(
            "WeasyPrint is not installed — skipping PDF generation for invoice %s. "
            "Install it with: pip install weasyprint",
            invoice_data.get("invoice_number", "UNKNOWN"),
        )
        return ""

    # ------------------------------------------------------------------
    # Build items rows
    # ------------------------------------------------------------------
    items_html_parts: list[str] = []
    for item in invoice_data.get("items", []):
        items_html_parts.append(
            f"<tr>"
            f"<td>{item['description']}</td>"
            f"<td style='text-align:right'>{item['quantity']}</td>"
            f"<td style='text-align:right'>{invoice_data['currency']} {item['unit_price']}</td>"
            f"<td>{invoice_data['currency']} {item['amount']}</td>"
            f"</tr>"
        )
    items_html = "\n      ".join(items_html_parts) if items_html_parts else (
        "<tr><td colspan='4' style='color:#999'>No items</td></tr>"
    )

    # ------------------------------------------------------------------
    # Optional rows (tax / discount)
    # ------------------------------------------------------------------
    tax_row = ""
    if float(invoice_data.get("tax_rate", 0)) > 0:
        tax_row = (
            f"<tr>"
            f"<td>Tax ({invoice_data['tax_rate']}%)</td>"
            f"<td>{invoice_data['currency']} {invoice_data['tax_amount']}</td>"
            f"</tr>"
        )

    discount_row = ""
    if float(invoice_data.get("discount_amount", 0)) > 0:
        discount_row = (
            f"<tr>"
            f"<td>Discount</td>"
            f"<td>&minus;{invoice_data['currency']} {invoice_data['discount_amount']}</td>"
            f"</tr>"
        )

    notes_section = ""
    if invoice_data.get("notes"):
        notes_section = (
            f"<div class='notes'>"
            f"<strong>Notes</strong>"
            f"{invoice_data['notes']}"
            f"</div>"
        )

    client_company_line = invoice_data.get("client_company", "")
    if client_company_line:
        client_company_line = f"{client_company_line}<br>"

    # ------------------------------------------------------------------
    # Render template
    # ------------------------------------------------------------------
    html_content = INVOICE_HTML_TEMPLATE.format(
        invoice_number=invoice_data["invoice_number"],
        status=invoice_data["status"].upper(),
        issue_date=invoice_data["issue_date"],
        due_date=invoice_data["due_date"],
        client_name=invoice_data["client_name"],
        client_email=invoice_data["client_email"],
        client_company_line=client_company_line,
        items_html=items_html,
        currency=invoice_data["currency"],
        subtotal=invoice_data["subtotal"],
        tax_row=tax_row,
        discount_row=discount_row,
        total=invoice_data["total"],
        notes_section=notes_section,
    )

    # ------------------------------------------------------------------
    # Write PDF to disk
    # ------------------------------------------------------------------
    upload_dir = Path(settings.UPLOAD_DIR) / "pdfs"
    upload_dir.mkdir(parents=True, exist_ok=True)

    safe_number = invoice_data["invoice_number"].replace("-", "_").replace("/", "_")
    unique_suffix = uuid.uuid4().hex[:8]
    filename = f"invoice_{safe_number}_{unique_suffix}.pdf"
    pdf_path = str(upload_dir / filename)

    HTML(string=html_content).write_pdf(pdf_path)
    logger.info("PDF generated: %s", pdf_path)
    return pdf_path
