"""
PDF generation service using WeasyPrint.

Generates a styled HTML invoice and renders it to a PDF file stored on disk.
The returned path is saved to ``Invoice.pdf_url`` and served via the download
endpoint.

If WeasyPrint is not installed the function logs a warning and returns an
empty string so the rest of the application can continue to work without it
(e.g., during local development or testing).
"""

import logging
import uuid
from pathlib import Path

from app.config import settings

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# HTML template
# ---------------------------------------------------------------------------

