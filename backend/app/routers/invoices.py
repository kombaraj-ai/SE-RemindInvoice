@router.get("/{invoice_id}/pdf", summary="Download invoice PDF", response_model=None)
async def download_pdf(
    invoice_id: int,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> FileResponse | JSONResponse:
    """
    Download the PDF for an invoice. Generates it on demand if not yet created.
    """
    from app.services.pdf import generate_invoice_pdf

    invoice = invoice_service.get_invoice(db, invoice_id, current_user.id)

    # Generate on demand if not yet produced
    if not invoice.pdf_url:
        invoice_data: dict = {
            "invoice_number": invoice.invoice_number,
            "status": invoice.status.value,
            "issue_date": str(invoice.issue_date),
            "due_date": str(invoice.due_date),
            "client_name": invoice.client.name if invoice.client else "",
            "client_email": invoice.client.email if invoice.client else "",
            "client_company": (invoice.client.company_name or "") if invoice.client else "",
            "currency": invoice.currency,
            "subtotal": str(invoice.subtotal),
            "tax_rate": str(invoice.tax_rate),
            "tax_amount": str(invoice.tax_amount),
            "discount_amount": str(invoice.discount_amount),
            "total": str(invoice.total),
            "notes": invoice.notes,
            "items": [
                {
                    "description": item.description,
                    "quantity": str(item.quantity),
                    "unit_price": str(item.unit_price),
                    "amount": str(item.amount),
                }
                for item in invoice.items
            ],
        }
        pdf_path = generate_invoice_pdf(invoice_data)
        if not pdf_path:
            return JSONResponse(
                status_code=503,
                content={"detail": "PDF generation failed. Please try again."},
            )
        invoice.pdf_url = pdf_path
        db.commit()

    return FileResponse(
        path=invoice.pdf_url,
        media_type="application/pdf",
        filename=f"{invoice.invoice_number}.pdf",
    )
